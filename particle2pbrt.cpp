float weightANISO(float r){
	if(r >= 2 * H) return 0;
	float d = r / (2 * H);
	return 1 - d * d * d;
}
void preprocessing(){
	vec fklen; // fake length, only in preprocessing
	for(int j = 0; j < 3; j++)
		fklen[j] = max(diff[j] / 100, 2 * H);

	for(int i = 0; i < part_n; i++){
		int gx = ceil((pos[i].x - minpos.x) / fklen.x);
		int gy = ceil((pos[i].y - minpos.y) / fklen.y);
		int gz = ceil((pos[i].z - minpos.z) / fklen.z);
		fakegrid[gx][gy][gz].push_back(i);
	}

	for(int i = 0; i < part_n; i++){
		int neighborN = 0;
		float sumwij = 0;
		double cov[3][3];
		for(int l = 0; l < 3; l++)
			for(int m = 0; m < 3; m++)
				cov[l][m] = 0;

		newpos[i] = vec(0, 0, 0);

		int gx = ceil((pos[i].x - minpos.x) / fklen.x);
		int gy = ceil((pos[i].y - minpos.y) / fklen.y);
		int gz = ceil((pos[i].z - minpos.z) / fklen.z);

		for(int dx = -1; dx < 2; dx++){
			if(gx + dx < 0) continue;
			for(int dy = -1; dy < 2; dy++){
				if(gy + dy < 0) continue;
				for(int dz = -1; dz < 2; dz++){
					if(gz + dz < 0) continue;
					for(auto j : fakegrid[gx + dx][gy + dy][gz + dz]){
						float wij = weightANISO(dist(pos[i] - pos[j]));
						if(wij == 0) continue;

						neighborN ++;
						sumwij += wij;
						newpos[i] = newpos[i] + wij * pos[j];
					}
				}
			}
		}

		newpos[i] = newpos[i] / sumwij; // Normalize

		for(int dx = -1; dx < 2; dx++){
			if(gx + dx < 0) continue;
			for(int dy = -1; dy < 2; dy++){
				if(gy + dy < 0) continue;
				for(int dz = -1; dz < 2; dz++){
					if(gz + dz < 0) continue;
					for(auto j : fakegrid[gx + dx][gy + dy][gz + dz]){
						float wij = weightANISO(dist(pos[i] - pos[j]));
						if(wij == 0) continue;

						vec delta = (pos[j] - newpos[i]);
						for(int l = 0; l < 3; l++)
							for(int m = 0; m < 3; m++)
								cov[l][m] += wij * delta[l] * delta[m];
					}
				}
			}
		}

		for(int l = 0; l < 3; l++)
			for(int m = 0; m < 3; m++)
				cov[l][m] /= sumwij; // Normalize

		//printf("#neigh:%d sumwij:%f\n", neighborN, sumwij);
		//printf("pos: %f %f %f\n", pos[i].x, pos[i].y, pos[i].z);
		//printf("npos: %f %f %f\n", newpos[i].x, newpos[i].y, newpos[i].z);
		//for(int l = 0; l < 3; l++){
		//	for(int m = 0; m < 3; m++)
		//		printf("%f ", cov[l][m]);
		//	printf("\n");
		//}

		double EIV[3][3], eig[3];
		eigen_decomposition(cov, EIV, eig);

		//printf("EigenVector&Value\n");
		//for(int j = 0; j < 3; j++){
		//	printf("%d: %f %f %f -> %f\n", j + 1, EIV[0][j], EIV[1][j], EIV[2][j], eig[j]);
		//}

		// We don't want the ellipsoid to be too flat
		eig[0] = max(eig[0], eig[2] / 5);
		eig[1] = max(eig[1], eig[2] / 5);

		// If Neighborhood is too small, use smaller sphere instead
		if(neighborN < 35)
			eig[0] = eig[1] = eig[2] = 0.6;
		// Else we want the transformation to be roughly volume-preserving
		else
			for(int j = 0; j < 3; j++) eig[j] *= Kscale;

		//for(int j = 0; j < 3; j++)
		//	printf("%f ", eig[j]);
		//printf("\n");

		float M[3][3]; // For bounding box of ellipsoid
		detG[i] = 1;
		for(int l = 0; l < 3; l++){
			for(int m = 0; m < 3; m++){
				M[l][m] = EIV[l][m] * eig[m] * H;
				G[i][l][m] = EIV[m][l] / eig[l] / H;
			}
			detG[i] *= eig[l] / H;
		}

		//printf("Identity: (For Debug Purpose)\n");
		for(int l = 0; l < 3; l++){
			for(int m = 0; m < 3; m++){
				float val = 0;
				for(int k = 0; k < 3; k++)
					val += M[l][k] * G[i][k][m];
				//printf("%f ", val);
			}
			//printf("\n");
		}

		vec halfbox = vec(dist(vec(M[0][0], M[0][1], M[0][2]))
				, dist(vec(M[1][0], M[1][1], M[1][2]))
				, dist(vec(M[2][0], M[2][1], M[2][2])));
		bbox[i][0] = newpos[i] - halfbox;
		bbox[i][1] = newpos[i] + halfbox;

		//printf("Bounding Box:\n");
		//printf("%f %f %f\n", bbox[i][0].x, bbox[i][0].y, bbox[i][0].z);
		//printf("%f %f %f\n", bbox[i][1].x, bbox[i][1].y, bbox[i][1].z);
		//printf("\n");
	}

	for(int i = 0; i < part_n; i++){
		int gx = ceil((pos[i].x - minpos.x) / fklen.x);
		int gy = ceil((pos[i].y - minpos.y) / fklen.y);
		int gz = ceil((pos[i].z - minpos.z) / fklen.z);
		fakegrid[gx][gy][gz].pop_back();
	}
}

