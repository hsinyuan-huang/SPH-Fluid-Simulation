int VertexInterp(string iA, string iB){
	if(edgemap.count(iA + iB) == 0){
		if(valuemap.count(iA) == 0)
			valuemap[iA] = 0;
		float vA = valuemap[iA];
		if(valuemap.count(iB) == 0)
			valuemap[iB] = 0;
		float vB = valuemap[iB];

		int l, m, n;
		sscanf(iA.c_str(), "%d:%d:%d", &l, &m, &n);
		vec pA = vec(l, m, n) * baselen + minpos;
		sscanf(iB.c_str(), "%d:%d:%d", &l, &m, &n);
		vec pB = vec(l, m, n) * baselen + minpos;
		
		vec p;
		if(fabs(vA - vB) > 1e-5)
			p = pA + (pB - pA) * (isolevel - vA) / (vB - vA);
		else
			p = pA;

		edgepos.push_back(p);
		edgemap[iA + iB] = edgecnt ++;
	}
	return edgemap[iA + iB];
}

